#include <unistd.h>

void	ft_putnbr(int nb)
{
	long n1;
	long n2;

	n1 = nb;
	n2 = 0;
	if (n1 < 0)
	{
		n1 = n1 * (-1);
		write(1, "-", 1);
	}
	if (n1 / 10 > 0)
	{
		ft_putnbr(n1 / 10);
	}
	n2 = (n1 % 10) + '0';
	write(1, &n2, 1);
}
