#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int c;
	int i;

	c = 0;
	i = 0;
	if (*str == '\0')
	{
		return (1);
	}
	while (*str != '\0' && i == 0)
	{
		if ((*str >= 'A' && *str <= 'Z') || (*str >= 'a' && *str <= 'z'))
		{
			c = 1;
		}
		else
		{
			c = 0;
			i = 1;
		}
		str++;
	}
	return (c);
}
