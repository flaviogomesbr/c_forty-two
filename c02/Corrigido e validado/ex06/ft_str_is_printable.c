#include <unistd.h>

int	ft_str_is_printable(char *str)
{
	int c;
	int i;

	c = 0;
	i = 0;
	if (*str == '\0')
	{
		return (1);
	}
	while (*str != '\0' && i == 0)
	{
		if (*str >= 32 && *str <= 127)
		{
			c = 1;
		}
		else
		{
			c = 0;
			i = 1;
		}
		str++;
	}
	return (c);
}
